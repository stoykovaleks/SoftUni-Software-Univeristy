function monthPrinter(monthDay) {
    if (monthDay == 1){
        console.log ("January");
    }
    else if (monthDay == 2){
        console.log ("February");
    }
    else if (monthDay == 3){
        console.log ("March");
    }
    else if (monthDay == 4){
        console.log ("April");
    }
    else if (monthDay == 5){
        console.log ("May");
    }
    else if (monthDay == 6){
        console.log ("June");
    }
    else if (monthDay == 7){
        console.log("July");
    }
    else if (monthDay == 8){
        console.log ("August");
    }
    else if (monthDay == 9){
        console.log ("September");
    }
    else if (monthDay == 10){
        console.log ("October");
    }
    else if (monthDay == 11){
        console.log ("November");
    }
    else if (monthDay == 12){
        console.log ("December");
    }
    else{
        console.log ("Error!")
    }
}

monthPrinter([4])